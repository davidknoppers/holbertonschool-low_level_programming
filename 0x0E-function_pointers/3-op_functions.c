#include "calc.h"
/**
 * op_add - adds ints
 *
 * @a: first int
 * @b: second int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts ints
 *
 * @a: first int
 * @b: second int
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies ints
 *
 * @a: first int
 * @b: second int
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - adds ints
 *
 * @a: first int
 * @b: second int
 * Return: quotient, rounded down
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divides ints and returns remainder
 *
 * @a: first int
 * @b: second int
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
